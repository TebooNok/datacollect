package com.pginfo.datacollect.dao;

public class User {
    private String id;
    private String name;
    private String password;
    private String role;
    public String getid(){
        return id;
    }
    public String getname(){
        return name;
    }
    public String getpassword(){
        return password;
    }
    public String getrole(){
        return role;
    }

}
